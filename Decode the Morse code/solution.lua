    [".--.-."] = "@",
    ["...---..."] = "SOS"
}

local kata = {}
function kata.decode_morse_code(morse_code)
    result = morse_code:match("^%s*(.-)%s*$")
    result = result:gsub("[^%s]+", MORSE_CODE)
    result = result:gsub("%s%s%s", '<tripplespace>')
    result = result:gsub("%s", '')
    result = result:gsub("<tripplespace>", ' ')

    return result
end

return kata
