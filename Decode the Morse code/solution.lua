    [".-."] = "R",
    ["..."] = "S",
    ["-"] = "T",
    ["..-"] = "U",
    ["...-"] = "V",
    [".--"] = "W",
    ["-..-"] = "X",
    ["-.--"] = "Y",
    ["--.."] = "Z",
    ["-----"] = "0",
